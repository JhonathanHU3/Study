/* 
Operadores Lógicos
&& -> AND -> E
|| -> OR -> OU
! -> NOT -> NÃO
*/
console.log(true && true && true); // todas as expressões precisam ser verdadeiras para retornar true
console.log(true || false || true); // apenas uma expressão precisa ser verdadeira para retornar true
console.log(!false); // inverte completamento o valor da expressão